package model;

public class DeviceStatus {
	private String udid;
	private String status;
	
	public DeviceStatus() {
		
	}
	
	public String getUdid() {
		return udid;
	}
	public void setUdid(String udid) {
		this.udid = udid;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	
}
